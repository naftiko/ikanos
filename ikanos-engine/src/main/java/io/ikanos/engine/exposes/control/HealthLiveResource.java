package io.ikanos.engine.exposes.control;

import org.restlet.data.MediaType;
import org.restlet.representation.Representation;
import org.restlet.representation.StringRepresentation;
import org.restlet.resource.Get;
import org.restlet.resource.ServerResource;

/**
 * Liveness probe. Returns {@code 200 {"status":"UP"}} when the process is alive.
 */
public class HealthLiveResource extends ServerResource {

    @Get("json")
    public Representation getLive() {
        return new StringRepresentation("{\"status\":\"UP\"}", MediaType.APPLICATION_JSON);
    }
}
